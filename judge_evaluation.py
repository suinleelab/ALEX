#!/usr/bin/env python3
"""judge_evaluation.py

Run independent judging/scoring for feature explanations generated by clinical_agent.py.

This script is intentionally decoupled from generation. It reads an existing
explanations JSON and writes judge outputs in the same format used previously:
- <base>_judge_original.json
- <base>_judge_revised.json (if revised explanations are provided/found)

Examples:
  python judge_evaluation.py \
    --explanations_json docs/agent/ist3/gpt-5-mini/with_shap_drlearner/explanations.json \
    --shap_json results/ist3/shapley/ist3_shap_summary_False.json \
    --trial_name ist3 \
    --model gpt-5

  python judge_evaluation.py \
    --explanations_json docs/agent/ist3/gpt-5-mini/simple_cot/explanations.json \
    --trial_name ist3 \
    --model gpt-5-mini \
    --api_provider openrouter
"""

import argparse
import json
import os
from typing import List, Optional

from openai import OpenAI
from pydantic import BaseModel, Field, computed_field
from src.agent_schemas import ArticleExtraction
from src.agent_utils import (
    _convert_hypogenic_to_feature_format,
    _is_hypogenic_format,
    get_dataset_features,
    get_model_client,
    get_trial_metadata,
    infer_revised_path,
    load_json_file,
    load_local_env,
    load_top_features,
    map_feature_label,
    search_and_extract_article,
    write_json_file,
)


class FeatureExplanationScoreWithMechanisms(BaseModel):
    feature_name: str = Field(..., description="Feature being scored")
    explanation_id: Optional[str] = Field(None, description="Unique identifier for this explanation")

    # Chain-of-thought scratchpad — fill this BEFORE the boolean gates.
    # Write one sentence per gate explaining your reasoning, then set each boolean accordingly.
    gate_reasoning: str = Field(
        ...,
        description=(
            "Required reasoning scratchpad. Before answering each gate, write one sentence "
            "justifying your TRUE/FALSE decision for that gate in order: "
            "G1 (feature measured?), "
            "G2 (concrete biological pathway stated — not just 'higher risk → more benefit'?), "
            "G3 (genuine HTE mechanism — not purely absolute-risk amplification, not post-treatment variable, not reverse-causality?), "
            "G4 (actionable subgroups?), G5 (literature support?). "
            "If a gate is FALSE, state why and note that all subsequent gates will also be FALSE."
        ),
    )

    # The Optimized Robustness Gates
    is_observed_in_data: bool = Field(..., description="Gate 1: Is the explanation based on features actively measured in the trial?")
    is_biologically_coherent: bool = Field(
        ...,
        description=(
            "Gate 2 (Biological Logic): Does the explanation describe a concrete biological pathway "
            "that mechanistically connects this feature to differential drug action? "
            "TRUE only if specific physiological steps are stated (e.g., a named receptor, enzyme, "
            "pharmacokinetic change, or cell-type response). "
            "FALSE if: (a) only a statistical/epidemiological claim is made with no biological "
            "mechanism (e.g., 'higher-risk patients benefit more', 'differential baseline risk'), "
            "(b) the reasoning is circular or tautological (feature mentioned but no causal chain), "
            "or (c) the proposed pathway contradicts established medical science."
        ),
    )
    is_causally_plausible: bool = Field(
        ...,
        description=(
            "Gate 3 (Genuine HTE vs Statistical Artifact): Is the proposed mechanism a true "
            "treatment effect modifier — the drug actually works *differently* in this subgroup — "
            "rather than a statistical artifact, proxy, or confound? "
            "TRUE only if the explanation claims a specific PD/PK or pathophysiological difference "
            "in drug action for this subgroup. "
            "FALSE if: (a) the SOLE argument is absolute-risk amplification (higher baseline event "
            "rate × constant RRR = larger absolute benefit) with no additional mechanistic pathway; "
            "(b) the feature is post-randomization or post-treatment (e.g., 'received transfusion', "
            "'on-treatment SBP'); (c) reverse-causality — outcome severity causes the feature; "
            "or (d) the feature is a trivial severity proxy with no additive mechanism beyond "
            "predicting prognosis."
        ),
    )
    is_clinically_actionable: bool = Field(..., description="Gate 4: Does this propose clear, actionable subgroups for real-world treatment?")
    is_literature_backed: bool = Field(..., description="Gate 5: Is this specific interaction supported by existing published clinical literature?")

    # Bonus/Metadata
    is_novel: bool = Field(..., description="Bonus: Does it provide novel insights beyond existing literature?")

    justification: str = Field(..., description="One-sentence overall justification summarising which gate was the decisive pass/fail and why.")

    @computed_field
    @property
    def overall_score(self) -> int:
        """
        Strict cascading AND operation.
        If any earlier gate fails (0), the subsequent additions are nullified.
        """
        c1 = int(self.is_observed_in_data)
        c2 = int(self.is_biologically_coherent)
        c3 = int(self.is_causally_plausible)
        c4 = int(self.is_clinically_actionable)
        c5 = int(self.is_literature_backed)

        # Base score + cascading bonuses. Maximum score is 5.
        return c1 + (c1*c2) + (c1*c2*c3) + (c1*c2*c3*c4) + (c1*c2*c3*c4*c5)

class FeatureJudgeOutput(BaseModel):
    scored_features: List[FeatureExplanationScoreWithMechanisms]


def _build_study_context(
    explanations_payload: dict,
    treatment: str,
    outcome: str,
    population: str,
    evidence: dict,
) -> dict:
    feature_explanations = explanations_payload.get("feature_explanations", [])
    n_features = explanations_payload.get("n_unique_features", len({f.get("feature_name") for f in feature_explanations}))
    n_explanations_per_feature = explanations_payload.get("n_explanations_per_feature", 0)
    if not n_explanations_per_feature and feature_explanations:
        n_explanations_per_feature = max(
            len((feature_explanations[0] or {}).get("mechanisms", []) or []),
            0,
        )

    dataset = explanations_payload.get("dataset", evidence.get("dataset", "unknown_dataset"))

    # Prefer features from the SHAP JSON evidence; fall back to the canonical
    # per-dataset registry so Gate 1 always has a ground-truth reference.
    available_features = evidence.get("available_features") or get_dataset_features(dataset)

    return {
        "dataset": dataset,
        "learner": explanations_payload.get("learner", "unknown_learner"),
        "population": population,
        "treatment": treatment,
        "outcome": outcome,
        "source_explainer": explanations_payload.get("source_explainer", evidence.get("explainer", "unknown_explainer")),
        "n_explanations_per_feature": n_explanations_per_feature,
        "n_features": n_features,
        "available_features": available_features,
    }


def _normalize_feature_names_for_judge(explanations_payload: dict, dataset_name: Optional[str]) -> None:
    """Map raw feature names to clinician-readable labels in-place for judge scoring.

    This only affects what is sent to the judge and does not modify source explanation files.
    """
    feature_explanations = explanations_payload.get("feature_explanations") or []
    for feature_item in feature_explanations:
        if not isinstance(feature_item, dict):
            continue
        raw_name = feature_item.get("feature_name")
        if not isinstance(raw_name, str) or not raw_name.strip():
            continue
        feature_item["feature_name"] = map_feature_label(raw_name, dataset_name)


def score_feature_explanations(
    explanations: list,
    study_context: dict,
    client: OpenAI,
    model_name: str,
    article_extraction: Optional[ArticleExtraction] = None,
    is_revised: bool = False,
) -> Optional[FeatureJudgeOutput]:
    """Score feature explanations using an independent judge."""
    judge_system = (
        "You are an independent scientific judge for mechanistic explanations.\n"
        "Evaluate each explanation using a strict cascading gate framework.\n"
        "Be objective, fair, and constructive.\n"
        "\n"
        "IMPORTANT: Score EACH explanation entry INDIVIDUALLY. Entries may share the same\n"
        "feature_name when multiple explanations target the same feature (e.g. different subgroups).\n"
        "In your output, copy the 'explanation_id' field from each input entry so every scored\n"
        "result can be traced back to its source explanation.\n"
        "\n"
        "STEP-BY-STEP EVALUATION PROCESS (follow this order for EACH explanation):\n"
        "1. Write gate_reasoning: one sentence per gate (G1→G5) before committing to any boolean.\n"
        "   G2: ask 'is a named biological pathway stated — receptor, enzyme, PK change?'\n"
        "       NOT just 'higher baseline risk → more absolute benefit'.\n"
        "   G3: ask 'does the drug act differently here, beyond pure absolute-risk amplification?\n"
        "       Is this a baseline feature (not post-treatment)? No reverse causality?'\n"
        "   If a gate fails, explicitly state 'G[N] FALSE — cascade: G[N+1..5] also FALSE.'\n"
        "2. Set each boolean consistent with your gate_reasoning.\n"
        "3. Write justification: one sentence naming the decisive gate and why.\n"
        "\n"
        "EVALUATION FRAMEWORK — 5 CASCADING ROBUSTNESS GATES\n"
        "\n"
        "Each gate is a strict boolean (true/false). Gates must be evaluated IN ORDER.\n"
        "An explanation that fails an earlier gate cannot earn credit for later gates.\n"
        "The overall_score is computed automatically as:\n"
        "  score = G1 + G1*G2 + G1*G2*G3 + G1*G2*G3*G4 + G1*G2*G3*G4*G5  (max = 5)\n"
        "\n"
        "Gate 1 — is_observed_in_data (Measurement Validity):\n"
        "  The judge_prompt includes a key 'trial_measured_features' listing EVERY column that was\n"
        "  actually recorded in this trial dataset. Use it as the authoritative ground-truth.\n"
        "  TRUE if: The proposed modifying feature (or a close synonym) appears in trial_measured_features.\n"
        "  FALSE if: The feature is absent from trial_measured_features, inferred post-hoc, or derived\n"
        "            from data not collected in this trial.\n"
        "  Note: If FALSE, all subsequent gates must also be FALSE (cascade).\n"
        "\n"
        "Gate 2 — is_biologically_coherent (Biological Logic):\n"
        "  Evaluate the QUALITY of the proposed mechanism, not just its absence of error.\n"
        "  TRUE only if: The explanation names concrete biological steps — a receptor, enzyme,\n"
        "    PK/PD change, or cell pathway — that mechanistically connect the feature to\n"
        "    differential drug action. Vague statements that merely predict risk do NOT qualify.\n"
        "  FALSE if any of the following:\n"
        "    (a) No biological pathway is described — only a statistical/epidemiological claim:\n"
        "        e.g., 'X → higher baseline risk → more absolute benefit'. This includes hollow\n"
        "        phrases like 'X may modify treatment through differential baseline risk' or\n"
        "        'variation in X alters event-risk trajectories' with no named mechanism.\n"
        "    (b) Circular or tautological: feature referenced but no causal chain given for\n"
        "        why the drug acts differently in this subgroup.\n"
        "    (c) Biologically backward or physically impossible pathway (e.g., a beta-blocker\n"
        "        raises heart rate, statins elevate LDL).\n"
        "  Note: If FALSE, gates 3-5 must also be FALSE.\n"
        "\n"
        "Gate 3 — is_causally_plausible (Genuine HTE vs Statistical Artifact):\n"
        "  Confirm the mechanism is a TRUE treatment effect modifier — the drug works\n"
        "  differently in this subgroup — rather than a statistical artifact or proxy.\n"
        "  TRUE only if: The explanation claims a specific PD, PK, or pathophysiological\n"
        "    difference in how the drug acts for this subgroup.\n"
        "    Good examples: 'CKD reduces TXA renal clearance → higher drug exposure';\n"
        "    'hyperglycaemia amplifies reperfusion injury → greater sICH risk with alteplase';\n"
        "    'low renin state in Black patients → blunted ACEi response'.\n"
        "  FALSE if any of the following:\n"
        "    (a) Absolute-risk amplifier only: The SOLE argument is that the feature raises\n"
        "        baseline event rate so the same relative risk reduction yields a larger\n"
        "        absolute benefit. This is arithmetic, not HTE. Mark FALSE unless an\n"
        "        additional drug-mechanism pathway is also stated.\n"
        "        Bad example: 'CKD → higher CV risk → larger absolute benefit from BP lowering.'\n"
        "        Good (pass): 'CKD impairs renal autoregulation → BP lowering risks AKI,\n"
        "          so net benefit follows a U-curve depending on eGFR.'\n"
        "    (b) Post-randomization / post-treatment variable: the feature is determined\n"
        "        after treatment starts (e.g., 'received transfusion', 'on-treatment SBP',\n"
        "        'experienced adverse event').\n"
        "    (c) Reverse-causality: the outcome or its severity determines the feature\n"
        "        value, not the other way around.\n"
        "    (d) Trivial severity proxy: the feature only predicts prognosis and adds\n"
        "        no pathway beyond absolute-risk amplification.\n"
        "  Note: If FALSE, gates 4-5 must also be FALSE.\n"
        "\n"
        "Gate 4 — is_clinically_actionable (Practical Utility):\n"
        "  TRUE if: The explanation defines clear, operationalizable patient subgroups with\n"
        "           distinct treatment recommendations that could be used in clinical practice.\n"
        "  FALSE if: Subgroups are vague, unmeasurable in routine care, or clinically insignificant.\n"
        "  Note: If FALSE, gate 5 must also be FALSE.\n"
        "\n"
        "Gate 5 — is_literature_backed (External Evidence):\n"
        "  TRUE if: The specific feature × treatment interaction (HTE) is supported by published\n"
        "           clinical literature — ideally RCT subgroup analyses or meta-analyses.\n"
        "  FALSE if: The interaction is speculative, contradicted by literature, or not studied.\n"
        "\n"
        "NOVELTY BONUS — is_novel:\n"
        "  TRUE if: The explanation identifies an underexplored mechanism or subgroup not already\n"
        "           well-covered in existing clinical guidelines or major reviews.\n"
        "  This is metadata only and does not affect overall_score.\n"
        "\n"
        "If original trial article context is provided, use it to:\n"
        "- Verify that proposed features were actually measured in the trial (Gate 1)\n"
        "- Confirm the proposed physiological pathway is biologically coherent (Gate 2)\n"
        "- Assess whether the feature is a genuine cause or trivial proxy in this trial (Gate 3)\n"
        "- Cross-check proposed interactions with trial-reported subgroup analyses (Gate 5)\n"
        "\n"
        "Provide honest, rigorous justification. Default to FALSE when in doubt."
    )

    judge_prompt = {
        "evidence": {
            "study_context": study_context,
        },
        # Authoritative list of every column measured in this trial — used for Gate 1.
        # Gate 1 is TRUE only when the proposed feature (or a direct synonym) appears here.
        "trial_measured_features": study_context.get("available_features", []),
        "feature_explanations_to_score": explanations,
        "scoring_instructions": {
            "be_objective": True,
            "framework": "5-gate cascading boolean evaluation (overall_score computed automatically)",
            "provide_justification": True,
            "default_on_uncertainty": "FALSE — only mark TRUE when clearly supported",
            "gate_1_note": (
                "Check 'trial_measured_features' above before answering is_observed_in_data. "
                "If the list is non-empty, only mark TRUE for features present in that list."
            ),
        },
    }

    if is_revised:
        judge_prompt["scoring_instructions"]["note"] = "These are REVISED explanations after verification"

    if article_extraction is not None:
        judge_prompt["trial_article_context"] = article_extraction.model_dump()
        judge_prompt["scoring_instructions"]["use_article_context"] = (
            "Use trial article context to inform scoring, especially for "
            "mechanism plausibility and clinical interpretation accuracy."
        )

    try:
        result = client.beta.chat.completions.parse(
            model=model_name,
            messages=[
                {"role": "system", "content": judge_system},
                {"role": "user", "content": json.dumps(judge_prompt, indent=2)},
            ],
            response_format=FeatureJudgeOutput,
        )
        return result.choices[0].message.parsed
    except Exception as e:
        print(f"Error scoring feature explanations: {e}")
        return None


def main() -> None:
    load_local_env()

    parser = argparse.ArgumentParser()
    parser.add_argument("--explanations_json", required=True, help="Path to original explanations JSON")
    parser.add_argument("--revised_json", help="Optional path to revised explanations JSON")
    parser.add_argument("--shap_json", help="Optional SHAP summary JSON used for evidence context")

    parser.add_argument("--trial_name", help="Trial name (ist3, crash_2, sprint, accord, txa)")
    parser.add_argument("--treatment", help="Manual treatment override")
    parser.add_argument("--outcome", help="Manual outcome override")
    parser.add_argument("--population", help="Manual population override")
    parser.add_argument("--dataset", help="Optional dataset override")

    parser.add_argument("--model", default="gpt-5-mini", help="Judge model")
    parser.add_argument(
        "--api_provider",
        default="openai",
        choices=["openai", "openrouter"],
        help="API provider",
    )
    parser.add_argument("--api_base_url", help="Optional custom API base URL")
    parser.add_argument(
        "--retrieve_article",
        action="store_true",
        help="Retrieve trial article context (requires --trial_name)",
    )

    parser.add_argument("--out_judge_original", help="Output path for original judge JSON")
    parser.add_argument("--out_judge_revised", help="Output path for revised judge JSON")

    args = parser.parse_args()

    if not os.path.exists(args.explanations_json):
        parser.error(f"explanations file not found: {args.explanations_json}")

    revised_path = args.revised_json or infer_revised_path(args.explanations_json)

    explanations_payload = load_json_file(args.explanations_json)
    if _is_hypogenic_format(explanations_payload):
        explanations_payload = _convert_hypogenic_to_feature_format(explanations_payload)
    if revised_path and args.revised_json and not os.path.exists(revised_path):
        parser.error(f"revised_json file not found: {revised_path}")
    revised_payload = load_json_file(revised_path) if revised_path and os.path.exists(revised_path) else None
    if revised_payload and _is_hypogenic_format(revised_payload):
        revised_payload = _convert_hypogenic_to_feature_format(revised_payload)

    trial_meta = get_trial_metadata(args.trial_name) if args.trial_name else None

    treatment = args.treatment or (trial_meta["treatment"] if trial_meta else explanations_payload.get("treatment"))
    outcome = args.outcome or (trial_meta["outcome"] if trial_meta else explanations_payload.get("outcome"))
    population = args.population or (trial_meta["population"] if trial_meta else explanations_payload.get("population"))

    if not all([treatment, outcome, population]):
        parser.error(
            "Could not resolve treatment/outcome/population. Provide --trial_name or manual --treatment --outcome --population."
        )

    dataset_override = args.dataset or (args.trial_name.lower() if args.trial_name else explanations_payload.get("dataset"))

    if args.shap_json and os.path.exists(args.shap_json):
        evidence = load_top_features(
            args.shap_json,
            n_features=max(1, len(explanations_payload.get("feature_explanations", []))),
            dataset_override=dataset_override,
        )
    else:
        evidence = {
            "dataset": dataset_override or explanations_payload.get("dataset", "unknown_dataset"),
            "learner": explanations_payload.get("learner", "unknown_learner"),
            "explainer": explanations_payload.get("source_explainer", "unknown_explainer"),
            "top_feature_evidence": [],
            "available_features": [],
        }

    # Build study context before normalising feature names to preserve accurate counts.
    study_context = _build_study_context(
        explanations_payload=explanations_payload,
        treatment=treatment,
        outcome=outcome,
        population=population,
        evidence=evidence,
    )

    # Normalise feature names in-place after study_context is built.
    _normalize_feature_names_for_judge(explanations_payload, dataset_override)
    if revised_payload is not None:
        _normalize_feature_names_for_judge(revised_payload, dataset_override)

    client = get_model_client(args.api_provider, args.api_base_url)

    article_extraction = None
    if args.retrieve_article:
        if not args.trial_name:
            print("Warning: --retrieve_article requires --trial_name. Skipping article retrieval.")
        else:
            article_query = trial_meta.get("article_query", f"{args.trial_name} clinical trial")
            print(f"Retrieving article information for {args.trial_name}...")
            article_extraction = search_and_extract_article(article_query, args.trial_name, client, model_name=args.model)
            if article_extraction:
                print("Successfully extracted article information.")

    base, ext = os.path.splitext(args.explanations_json)
    judge_suffix = f"__{args.model}" if args.model != "gpt-5-mini" else ""
    out_judge_original = args.out_judge_original or f"{base}_logic_score{judge_suffix}{ext}"
    out_judge_revised = args.out_judge_revised or f"{base}_logic_score_revised{judge_suffix}{ext}"

    print("Scoring original feature explanations...")
    judge_report = score_feature_explanations(
        explanations=explanations_payload.get("feature_explanations", []),
        study_context=study_context,
        client=client,
        model_name=args.model,
        article_extraction=article_extraction,
        is_revised=False,
    )

    if judge_report is None:
        raise RuntimeError("Failed to generate judge report for original explanations")

    write_json_file(out_judge_original, judge_report.model_dump())
    print(f"Wrote judge report (original) to: {out_judge_original}")

    if revised_payload is not None:
        print("Scoring revised feature explanations...")
        judge_report_revised = score_feature_explanations(
            explanations=revised_payload.get("feature_explanations", []),
            study_context=study_context,
            client=client,
            model_name=args.model,
            article_extraction=article_extraction,
            is_revised=True,
        )
        if judge_report_revised is None:
            raise RuntimeError("Failed to generate judge report for revised explanations")

        write_json_file(out_judge_revised, judge_report_revised.model_dump())
        print(f"Wrote judge report (revised) to: {out_judge_revised}")


if __name__ == "__main__":
    main()
